#!/usr/bin/env python3
"""Stringly token generator.

Emits the mechanical, one-line-per-token methods for the core library and the sample packs.
This is the "one-off script, commit the .cs" path from the design discussion: the token tables
change about once a decade, so a Source Generator would be overkill.

Re-run whenever a table below changes:

    python3 tools/generate.py

Generated files (all marked <auto-generated>):

  core   src/Stringly/Phrase.Tokens.g.cs   instance tokens on `partial class Phrase`
  core   src/Stringly/Say.Tokens.g.cs      static entry points on `partial class Say`
  core   src/Stringly/StringTokens.g.cs    string-extension twins (start a chain from a literal)
  nato   src/Stringly.Nato/Nato.g.cs       entries (class Start) + continuations (NatoExtensions)
  greek  src/Stringly.Greek/Greek.g.cs     entries (class Start) + continuations (GreekExtensions)
  emoji  src/Stringly.Emoji/Emoji.g.cs     entries (class Start) + continuations (EmojiExtensions)

Each pack's entry class is named `Start` (not after the namespace) so the import reads
`using static Stringly.Nato.Start;` rather than the stuttering `...Nato.Nato`.

Every token gets BOTH a static entry (so it can START a chain after `using static ...`) and an
instance continuation (so it can CONTINUE one). That symmetry is why no `Nothing()` seed is ever
needed — exactly how the core `Say`/`Phrase` pair already behaves.
"""

import os

HEADER = """// <auto-generated>
//     Generated by tools/generate.py — do not edit by hand.
//     Re-run: python3 tools/generate.py
// </auto-generated>
"""

# --- Core printable-ASCII table ------------------------------------------------------------------

SYMBOLS = {
    " ": "Space", "!": "Bang", '"': "Quote", "#": "Hash", "$": "Dollar", "%": "Percent",
    "&": "Amp", "'": "Apostrophe", "(": "LParen", ")": "RParen", "*": "Star", "+": "Plus",
    ",": "Comma", "-": "Dash", ".": "Period", "/": "Slash", ":": "Colon", ";": "Semicolon",
    "<": "Lt", "=": "Eq", ">": "Gt", "?": "Question", "@": "At", "[": "LBracket",
    "\\": "Backslash", "]": "RBracket", "^": "Caret", "_": "Underscore", "`": "Backtick",
    "{": "LBrace", "|": "Pipe", "}": "RBrace", "~": "Tilde",
}
DIGITS = ["Zero", "One", "Two", "Three", "Four", "Five", "Six", "Seven", "Eight", "Nine"]


def ascii_table():
    table = []
    for code in range(32, 127):
        ch = chr(code)
        if ch.isdigit():
            name = DIGITS[int(ch)]
        elif ch.isalpha():
            name = ch  # A() / a() — C# is case-sensitive, both legal and distinct
        else:
            name = SYMBOLS[ch]
        table.append((ch, name))
    return table


